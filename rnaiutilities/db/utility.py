"""
Database utility functions
"""


def feature_table_name(file):
    """
    Get the name of the table of a meta file.

    :param file: the name of the meta file
    :return: returns the name of the table
    """

    return file.replace("_meta.tsv", "").split("/")[-1].replace("-", "_")
